function solution(array, commands) {
    return commands.map(command => {
      const [startIdx, endIdx, selectIdx] = command;
      const newArr = array.filter((_, idx) => idx >= startIdx - 1 && idx < endIdx);
      newArr.sort((a, b) => a - b);

      return newArr[selectIdx - 1];
    })
}