function solution(n, lost, reserve) {
  let newReserve = reserve.filter(x => !lost.includes(x)).sort((a,b) => a - b);
  const newLost = lost.filter(x => !reserve.includes(x)).sort((a,b) => a - b);

  return n - newLost.filter(x => {
    const key = newReserve.find(r => Math.abs(r - x) === 1);

    if(!key) {
      return true;
    }

    newReserve = newReserve.filter(v => v !== key);
   }).length;
}